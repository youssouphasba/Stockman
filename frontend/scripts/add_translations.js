// For languages without specific translations, use English
const FALLBACK_LANGS = ['de', 'hi', 'it', 'pl', 'ro', 'tr'];

function deepMerge(target, source) {
  for (const key of Object.keys(source)) {
    if (source[key] && typeof source[key] === 'object' && !Array.isArray(source[key])) {
      if (!target[key]) target[key] = {};
      deepMerge(target[key], source[key]);
    } else if (!(key in target)) {
      // Only add if not already present
      target[key] = source[key];
    }
  }
  return target;
}

function processLocale(lang) {
  const filePath = path.join(LOCALES_DIR, `${lang}.json`);
  if (!fs.existsSync(filePath)) {
    console.log(`  Skipping ${lang}.json (not found)`);
    return;
  }

  const raw = fs.readFileSync(filePath, 'utf8').replace(/^\uFEFF/, '');
  const data = JSON.parse(raw);
  let keysForLang;

  if (lang === 'en' || lang === 'fr') {
    // Use the specific en/fr keys
    keysForLang = {};
    for (const section of Object.keys(NEW_KEYS)) {
      keysForLang[section] = NEW_KEYS[section][lang];
    }
  } else if (TRANSLATIONS[lang]) {
    keysForLang = TRANSLATIONS[lang];
  } else {
    // Fallback to English for other languages
    keysForLang = {};
    for (const section of Object.keys(NEW_KEYS)) {
      keysForLang[section] = NEW_KEYS[section].en;
    }
  }

  deepMerge(data, keysForLang);

  fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
  console.log(`  Updated ${lang}.json`);
}

