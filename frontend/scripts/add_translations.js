const allLangs = fs.readdirSync(LOCALES_DIR)
  .filter(f => f.endsWith('.json') && !f.includes('audit') && !f.includes('global'))
  .map(f => f.replace('.json', ''))
  .filter(l => !SKIP_LANGS.includes(l));

console.log(`Languages to process: ${allLangs.join(', ')}\n`);

for (const lang of allLangs) {
  processLocale(lang);
}

